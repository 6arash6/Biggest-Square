#include "bsq.h"

char	*ft_realloc(char *ptr, int old_size, int new_size)
{
	char	*new_ptr;
	int		i;

	new_ptr = (char *)malloc(new_size);
	if (!new_ptr)
		return (NULL);
	i = 0;
	while (i < old_size)
	{
		new_ptr[i] = ptr[i];
		i++;
	}
	if (ptr)
		free(ptr);
	return (new_ptr);
}

int	ft_isnumber(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int	ft_atoi(char *str)
{
	int	result;
	int	i;

	result = 0;
	i = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result);
}
